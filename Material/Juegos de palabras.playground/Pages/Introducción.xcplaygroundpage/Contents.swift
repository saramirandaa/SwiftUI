## Juego de palabras

 En este playground, pondrás en práctica tu conocimiento sobre valores, constantes y cadenas para crear divertidos juegos de palabras que puedes jugar con tus compañeros y amigos.
 
 Forma de juego: escribir un cuento, una broma, una cita u otra forma de narración corta (o buscar una ya escrita). Selecciona varias palabras o términos del texto que les pedirás a los participantes del juego que reemplacen con sus propios términos. El jugador elige valores para cada una de las palabras sin leer la historia. Luego, reemplazas las palabras de la narración por las palabras que eligió el jugador e imprimes la historia.
 
 Para que el texto final tenga sentido, debes tomar nota del rol que juega cada una de las palabras o los términos. Supongamos que eliges el siguiente texto:

 _Una medición precisa tiene el mismo valor que la opinión de mil expertos._ (Grace Hopper)

 Elegir algunas palabras clave y reemplazarlas por categorías gramaticales u otras etiquetas da como resultado algo así:

 _Una **<sustantivo>** **<adjetivo>** tiene el mismo valor que la **<sustantivo singular>** de **<número>** **<sustantivo plural>**._

 El jugador recibe la lista de términos y elige los valores al azar:

 **adjetivo**: ondulada\
 **sustantivo**: cuchara\
 **número**: 17\
 **adjetivo**: asombrosos\
 **sustantivo plural**: paraguas

 En este caso, imprimirías:

 Una cuchara ondulada tiene el mismo valor que 17 asombrosos paraguas. (Grace Hopper)

