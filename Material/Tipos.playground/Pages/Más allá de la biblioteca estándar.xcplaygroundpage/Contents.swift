## Más allá de la biblioteca estándar

 Los programadores también pueden crear sus propios tipos combinando y agregando los tipos y las funcionalidades de la librería estándar. A medida que avances en este curso, crearás tus propios tipos.

 - callout(Experimento): 
 Toma uno de los tipos que creaste en el experimento de la página anterior e imagina de qué tipos puede depender. Por ejemplo, `TrainingShoe` (calzado deportivo) puede tener un tipo `Int` (entero) para el talle, un tipo `String` (cadena) para la marca, un tipo `Date` (fecha) para su fecha de lanzamiento y otro tipo `Int` (entero) para su precio en dólares.

 Los tipos y las funcionalidades pueden agruparse en conjuntos denominados _marcos_ o _bibliotecas_. Al crear apps, puedes utilizar como base marcos incluidos en Xcode. Un marco muy importante es el marco *Foundation* (base).
 
 El marco Foundation incluye muchos tipos utilizados para representar elementos más específicos que solo cadenas o números de la biblioteca estándar de Swift. Por ejemplo, hay tipos para fechas, distancias y archivos en una computadora.
 
 Estos marcos adicionales no se agregan automáticamente a tus programas, ya que es posible que no los necesites. Sería como llevar todas tus pertenencias cuando vas de compras.
 
