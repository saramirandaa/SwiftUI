/*:
## Más comparaciones

 El operador de comparación `==` es muy útil, pero tener que verificar la igualdad en todos los números se volvería muy tedioso. Por suerte, existen más comparaciones para hacer entre los números:
 */
// Menos de
1 < 2
2 < 2
3 < 2

// Aumento de más de
1 > 2
2 > 2
3 > 2
//:  En algunas comparaciones se usan dos símbolos, uno junto al otro, que, al combinarse, tienen un único significado:
// No igual a
1 != 2
2 != 2
2 != 1

// Menor o igual que
2 <= 2
1 <= 2
3 <= 2

// Mayor o igual que
1 >= 2
2 >= 2
3 >= 2

