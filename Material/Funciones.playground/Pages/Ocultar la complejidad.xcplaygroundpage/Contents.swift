## Ocultar la complejidad

 Podría parecerte que imprimir la canción infantil usando funciones lleva más tiempo y es más complejo que usar el código original de la primera página de este playground. Eso es cierto si pretendes pensar en cada función individual al mismo tiempo. Sin embargo, la idea de usar funciones es separar los elementos en partes entendibles y reutilizables. Cada parte realiza una unidad de trabajo clara.

 Al trabajar en una app, nunca se observa cada línea de código por separado. Cuando llames a una función, sabrás _qué_ hace, pero no necesariamente _cómo_ lo hace. Por ejemplo, en esta página, todas las funciones para crear la canción ya están integradas en la página, por lo que puedes escribir la canción de manera muy sencilla:
