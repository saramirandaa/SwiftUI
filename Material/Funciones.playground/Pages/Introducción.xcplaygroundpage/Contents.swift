## Vestirte

 Imagina que un amigo te invita a ver una película. Dormiste hasta tarde, entonces le contestas: “Nos vemos ahí. Solo tengo que vestirme”.
 
 Tu amigo sabe a qué te refieres con “vestirte”, pero no conoce los detalles, por ejemplo, qué camiseta vas a ponerte.
 
 Ahora piensa en los detalles relacionados con todo lo que hiciste para vestirte hoy: decidir qué ponerte, tomar las prendas de un ropero o una cómoda, ponerte cada prenda.
 
 El término “vestirse” parece referirse a algo muy simple, pero, al considerar cada detalle que entra en juego cuando te vistes, entiendes que esa palabra representa una gran complejidad.

 Usar una palabra como “vestirse” sirve para referirse al proceso de ponerse ropa sin necesidad de comunicar cada detalle específico.

 Hacemos esto todo el tiempo. Frases como “ir al dentista”, “preparar un sándwich” o “ir a tomar un café” describen actividades de las que sería muy tedioso hablar si fuéramos a mencionar cada paso en detalle.
 
 En la Unidad 1, aprendiste sobre la *abstracción*: la idea de definir un modo más sencillo para referirse a complejidades para no tener que preocuparte por los detalles. Este concepto fundamental del desarrollo de software es el método que usan los programadores para evitar que los detalles y la complejidad los abrumen.

 En esta lección, aprenderás a definir y usar _funciones_, una de las maneras más básicas de definir las abstracciones en la programación.

 - callout(Experimento): Tómate unos minutos para pensar en otras frases simples que representan ideas o actividades mucho más complejas.

 A continuación, aprenderás cómo los programadores usan frases simples para referirse a operaciones más complejas.

