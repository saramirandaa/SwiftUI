## Caracteres
Por supuesto, un texto puede incluir otros elementos además de letras. También puede incluir signos de puntuación, números, símbolos e incluso espacios, los cuales se conocen como _caracteres_. Todas las _cadenas_ se componen de _caracteres_.

Las letras son caracteres:
## Aa Bb Cc Dd Ee
Y también lo son los signos de puntuación:
## ! ? . , ; :
Números:
## 1 2 3 4 5 6 7 8 9 0
Símbolos:
## # @ $ & © π * + - /
Incluso hay caracteres especiales, como espacios y tabulaciones:\
_(No puedes verlos, pero están ahí)._
## `                         `

Una `String` (cadena) se define correctamente como una *secuencia de caracteres*, un conjunto de cosas en un orden particular, como las cuentas que aparecen en la página anterior.

Ahora que ya conoces las partes, es momento de crear algunas cadenas.\
