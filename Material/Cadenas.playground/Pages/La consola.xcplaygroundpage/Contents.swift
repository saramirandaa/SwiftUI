## La consola

 Como programador, tienes acceso al “detrás de escena”, donde puedes ver y hacer cosas que los usuarios generalmente no pueden. Una de esas cosas es la _consola_. Su origen se remonta a los tiempos en que el único tipo de interacción informática era a través de la escritura y la visualización de texto.

 Probablemente conozcas la palabra consola en el contexto de los videojuegos y los soportes de TV. Sin embargo, en programación, la consola es un centro de mensajes para mostrar detalles sobre la forma en que se está ejecutando un programa.

### Mostrar la consola
 Para mostrar la consola, haz clic en este botón ubicado en el extremo inferior izquierdo de la ventana de Xcode:

 ![Botón Consola](console_button.png)

 o en el botón del medio ubicado en el extremo superior derecho de la ventana de Xcode

 ![Trío de botones para ocultar y mostrar paneles](xcode-show-debug-area.png)

 Muestra y oculta la consola algunas veces para practicar, pero déjala visible una vez que termines la práctica.
 
 Para ajustar la altura de la consola, mueve el mouse hacia la barra que se encuentra en la parte superior de la consola, hasta que veas que el cursor cambia a una barra horizontal con las flechas hacia arriba y hacia abajo. Luego, arrastra el cursor.

 ![Cursor vertical del mouse para ajustar el tamaño](resizeUpDownCursor.png)

### Impresión

 El comando `print` (imprimir) le indica al programa Swift que envíe un mensaje a la consola. Puedes usarlo con una cadena como la siguiente:

 `print("Probando: uno, dos, tres.")`

 - callout(Ejercicio): Prueba imprimir algunas de las cadenas que aparecen más abajo.
