## Cadenas
Mira cualquier app y verás que contiene texto, ya sean apps de mensajes, lectores de noticias, redes sociales, mapas u otras. Incluso las apps de cámara pueden tener texto para los nombres de botones y controles.

En la mayoría de los lenguajes de programación, incluido Swift, los valores de texto se conocen como _cadenas_.

### ¿Por qué “cadenas”?
Imagina una cadena de cuentas que forman un collar. Cada cuenta puede ser completamente diferente, pero todas están enlazadas en un orden particular.

![cadena de cuentas](StringOfBeads.png)
En general, una única letra (por ejemplo, una “H”) no es útil por sí sola. Pero una cadena de letras puede crear una palabra, por lo que los programadores llaman al texto una _cadena_. Una cadena puede ser cualquier texto que imagines.

![cadena de caracteres](StringOfCharacters.png)

A continuación, aprenderás qué otros elementos, además de las letras, pueden formar una cadena.

