## Elegir nombres útiles
La elección de nombres útiles es una habilidad de programación muy importante. Los programadores deben elegir nombres útiles para que sea fácil leer y entender el código.

Hasta el momento, has usado nombres como `numberOfDogs` (cantidad de perros) y `numberOfCats` (cantidad de gatos). Estos nombres son útiles porque describen con claridad lo que representan.

Imagina si los nombres hubieran sido `nd` y `nc` (para **n**umber of **d**ogs [cantidad de perros] y **n**umber of **c**ats [cantidad de gatos]).\
Imagina nombres aún menos descriptivos, como `n1` y `n2`.

Swift acepta sin problemas nombres como estos. Pero imagina si tuvieras que volver a leer ese código después de tus vacaciones. ¿Recordarías lo que significaban `nd` o `n1`? O si les mostraras el playground a otros programadores, ¿podrían descifrar el significado de esos nombres?

