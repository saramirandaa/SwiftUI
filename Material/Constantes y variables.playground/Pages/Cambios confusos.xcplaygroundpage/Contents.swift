## Cambios confusos

Cuando algo cambia en tu vida, puedes sentirte algo confundido.
 
Imagina que vas a reunirte con un grupo de seis amigos en el cine a las 20:00 h.

Te envían un mensaje para avisarte que hubo un cambio de planes. Van a mirar otra película en un cine diferente y se van a encontrar a las 21:00 h.

Un rato más tarde, recibes otro mensaje en el que te avisan que el plan volvió a cambiar. Van a quedarse en la casa de tu amigo y mirarán una película allí. La reunión ahora es a las 18:00 h, y van a pedir una pizza.
 
Luego, en otro mensaje, te avisan que van a estar en la casa de tu otro amigo, pero a las 18:30 h.

 - callout(Experimento): Con tantos cambios, ¿qué tan probable es que todos terminen en el lugar correcto a la hora indicada?\
\
¿No se reduciría la probabilidad de que surgieran errores si el plan no cambiara?

Cada vez que realizas un cambio, existe la posibilidad de que alguien que confiaba en esa información termine usando la información anterior y cometa un error.
 
Lo mismo ocurre en programación. Si conviertes algo en una constante, garantizas que ese valor no cambiará. No hay manera de que otra parte de tu programa se desincronice o use un valor antiguo.
 
 Sin embargo, en algunos casos, necesitas emplear variables. Hay aspectos de un programa que cambian mientras se ejecuta. En programación, algo que tiene la capacidad de cambiar se denomina _mutable_. Esta denominación proviene de la palabra _mutar_, que significa cambiar.
 
 Algo que no tiene la capacidad de cambiar se denomina _inmutable_. Si algo no necesita cambiar, te recomendamos usar una constante. Reducirás la probabilidad de que surjan errores o confusiones.

 Hablando de errores, sigue leyendo para aprender sobre un error común que se genera al trabajar con constantes y variables.
 
