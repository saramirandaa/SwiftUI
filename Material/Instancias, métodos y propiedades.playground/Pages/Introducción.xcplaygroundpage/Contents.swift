## Ejemplos de instancias

Ya aprendiste qué son los tipos en Swift y las semejanzas que tienen con los tipos de objetos de la vida real.
 
Para cada tipo de la vida real, hay muchas instancias físicas reales:

- Para el tipo “Automóvil”, existe un sinnúmero de variantes individuales en las calles. Cada uno tiene su kilometraje, marca, modelo, color, carga de combustible, y sus rayones y abolladuras. Cada automóvil tiene un conjunto particular de comportamientos: sus propios métodos para arrancar, frenar, pasar los cambios, etc.
 
 
- Para el tipo “Ciudad”, cada instancia de una ciudad tiene un nombre, una población, una ubicación y varios lugares emblemáticos. Además, cada ciudad tiene distintas formas de ocuparse de factores como responder a las emergencias o recolectar la basura.
 
El tipo define o describe las propiedades y los comportamientos de una clase de objeto en particular. Pero cada ejemplo concreto del tipo (cada automóvil o ciudad) es una instancia separada e independiente de ese tipo.
 
Piensa en dos automóviles de la misma marca y el mismo modelo, fabricados el mismo año. Uno está viajando a gran velocidad en una autopista y el otro está al costado del camino, sin combustible. Es el mismo tipo, pero dos instancias diferentes.
 
O imagina dos ciudades. La ubicación de cada ciudad es diferente. Un lugar emblemático de una ciudad puede atraer a muchos turistas, pero un lugar emblemático de la otra ciudad puede estar deteriorado y no recibir muchos visitantes. Ambas son ciudades, pero cada ciudad puede ser muy diferente de la otra.
 
En programación, puedes crear y usar distintas instancias de un tipo determinado. Cada instancia tiene su propio conjunto de valores de propiedades y cada instancia puede tener un comportamiento independiente de otras instancias.
 
