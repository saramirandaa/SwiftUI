 Cuando compilas apps en Swift, trabajas con distintas instancias de _struct_ (abreviatura de _structures_ [estructuras]) y _clases_. Ambos elementos ofrecen una forma de definir tipos en Swift.
 
 Por ahora, basta con saber que las structs y las clases tienen muchas características en común:

- Ambas tienen instancias
- Las instancias se crean con un inicializador
- Ambas pueden tener métodos
- Ambas pueden tener propiedades

Cuando creas y usas instancias, escribes el mismo código de Swift independientemente de que el tipo sea una estructura o una clase.
 
Durante lo que resta del curso, seguirás practicando cómo crear instancias y usar métodos y propiedades. En una próxima lección, aprenderás también cómo definir structs propias. Explorar las diferencias que existen entre las clases y las structs va más allá del alcance de este curso, pero, a medida que vayas ganando más experiencia con ambas, estarás mejor preparado para aprender más sobre estas diferencias en el futuro.

