- `"hello"` (hola) es una _instancia_ de un valor de tipo `String` (cadena).
 
Ya hace tiempo que vienes creando instancias del tipo `Int` (entero) y `String` (cadena). De hecho, cada valor que creas en un programa es una instancia de su tipo.
 
Las constantes `hello` (hola) y `aDifferentHello` (un saludo diferente) son dos instancias del tipo `String` (cadena), que casualmente tienen los mismos datos.
