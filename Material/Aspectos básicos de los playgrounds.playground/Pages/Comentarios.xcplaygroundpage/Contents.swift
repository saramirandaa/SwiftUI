/*:
## Comentarios
La claridad y la legibilidad son consideraciones importantes al momento de escribir código. Los programadores agregan pequeñas notas en el código para explicar lo que sucede.
Estas notas ayudan a otras personas a entender el código cuando lo leen.
Las notas se denominan _comentarios_.

El playground ignora los comentarios, y estos no afectan la ejecución del código.

Un comentario empieza con dos barras `//`:
 */
// Este es un comentario; no afecta el código.
34 + 56 + 230
//: Dado que los comentarios se ignoran, no se muestran resultados para esa línea de código en la barra de resultados.
// 200 + 34 + 45
/*:
 - callout(Experimento): 
Para probar _cómo dejar un comentario_ con el cálculo `34 + 56 + 230`, debes agregar dos barras al comienzo del cálculo.\
Para probar _cómo eliminar el comentario_ del cálculo `200 + 34 + 45`, elimina las dos barras.\
Edita algunas expresiones existentes y agrega otras propias.\
Observa que, cada vez que realizas un cambio, los resultados se actualizan en la barra lateral.
 */

/*:
 Los programadores suelen dejar o eliminar comentarios en el código cuando trabajan. Sin embargo, los comentarios tienen un uso mucho más importante. Al escribir código, también debes documentarlo. La documentación te ayuda a registrar por qué tomaste ciertas decisiones y a explicar cómo funciona el código. Podrías escribir ese material explicativo en un archivo separado, pero es mucho más fácil poder leer la documentación dentro del mismo código. Los programadores usan comentarios para documentar el código a medida que lo escriben.
 
 Por ejemplo, ¿qué representaban los cálculos antes mencionados? Con los comentarios, el código se aclara.
 */
