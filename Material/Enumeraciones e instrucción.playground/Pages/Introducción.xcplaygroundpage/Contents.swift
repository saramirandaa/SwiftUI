## Opciones múltiples

 En la vida, suelen presentarse situaciones en las que debes tomar decisiones. En muchos casos, debes elegir una opción entre varias y no puedes no elegir ninguna.
 
 Por ejemplo, es posible que un restaurante ofrezca un menú limitado con tres opciones de platos principales: pastas, hamburguesa o sopa.
 
 Este sistema funciona bien:

- El encargado del restaurante sabe exactamente qué ingredientes debe tener a mano todos los días, de modo que puede pedir las provisiones con anticipación.
- El personal de la cocina puede preparar y servir cada comida de forma rápida y eficiente.
- No pierden tiempo en intentar entender o en tomar pedidos especiales.
 
 Como programador, puedes sacar provecho de estas opciones controladas y aplicarlas a tus programas.
 
