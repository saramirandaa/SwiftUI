## Escritura de nombres y completado automático
Xcode realiza un seguimiento de todos los nombres que has definido. Entonces, cuando comienzas a escribir, ves un menú de nombres sugeridos (nombres que conoce Xcode). Esto se denomina _completado automático_.

El completado automático permite escribir código con más rapidez y menos errores de escritura. Cuanto más escribas, más se acota el menú de opciones de completado automático, lo que facilita tu búsqueda.

Cuando aparece el menú de opciones de completado automático, puedes:
- Seguir escribiendo para acotar la lista.
- Usar las teclas de flecha hacia arriba y abajo para subir y bajar en la lista.
- Presionar la tecla Retorno para que Xcode complete el nombre seleccionado.

![Menú emergente de completado automático](autocomplete.png)
En la lista, verás nombres que no definiste. Puedes ignorar estos nombres sin ningún problema.

