## Jerga de programadores
Desarrollar apps es más que saber qué escribir en Xcode. Los programadores trabajan en conjunto, comentan formas de resolver problemas y comparten sus respectivas explicaciones. Por eso, es importante aprender la terminología que usan los programadores.

A lo largo de este curso, no solo aprenderás a escribir código y a crear apps, sino también conocerás los términos que debes usar para hablar sobre código con otros desarrolladores.

En este playground, aprendiste sobre los términos _constante_ e _identificador_. Sigue leyendo para entender algunos términos adicionales.

En el primer playground, escribiste código como `6 + 2`, que es el ejemplo de una _expresión_. La computadora evalúa las expresiones para producir un valor.

Las expresiones en sí mismas conllevan una cantidad mínima de información. En este playground, agregaste el valor de las _instrucciones_. Las instrucciones se parecen más a enunciados o acciones (por lo general, contienen expresiones) y se suelen llamar, en términos informales, _líneas de código_.
 */
let numberOfDogs = 101
/*:
Cuando defines una constante en Swift, eso se denomina _declarar_ una constante. Por lo tanto, la instrucción anterior se conoce como _declaración_. La instrucción que le da a la computadora es “Declarar que el nombre `numberOfDogs` (cantidad de perros) tiene el valor `101`”.

La palabra `let` es un ejemplo de _palabra clave_. Cada palabra clave tiene un significado especial en Swift y no se puede usar como nombre.  La palabra clave `let` se usa para declarar una constante. Más adelante, conocerás más palabras clave de Swift.

La última parte de la instrucción, `= 101`, es donde _asignas_ un valor a la constante. Esto se denomina _asignación_, y el signo igual `=` se denomina _operador de asignación_.

 - callout(Nota): No te preocupes por memorizar cada término nuevo porque puede ser abrumador. Verás estos términos usados en contexto durante el resto del curso y, si necesitas refrescar la memoria, puedes buscarlos en el Glosario.

