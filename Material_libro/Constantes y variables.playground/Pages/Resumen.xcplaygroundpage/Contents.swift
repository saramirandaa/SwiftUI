## Resumen

 Aprendiste conceptos muy importantes en este playground:

- Los valores declarados con `let` son constantes y no pueden modificarse una vez que se asigna el valor. Estos valores se denominan _inmutables_.
- Los valores declarados con `var` son variables y es posible asignarles nuevos valores en el tiempo. Estos valores se denominan _mutables_.
- Un valor mutable puede utilizarse como parte de una instrucción de asignación a sí mismo: `score = score + 10` (puntaje = puntaje + 10).
- Los operadores de asignaciones compuestas permiten la actualización de los valores mutables: `score += 10` (puntaje += 10).
- El uso de constantes y variables en los lugares correctos ayuda a que tu código sea más seguro y fácil de entender.
 
