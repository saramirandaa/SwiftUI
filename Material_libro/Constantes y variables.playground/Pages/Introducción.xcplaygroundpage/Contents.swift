 Es posible que ya hayas deducido que el nombre “constante” tiene que ver con permanecer igual (es decir, permanecer constante) en el tiempo. Una vez que asignas el valor de una constante, puedes confiar en que el valor será siempre el mismo.
 
 Existe una segunda manera de asociar un valor con un nombre en Swift. Estos valores se denominan _variables_. Con una variable, el valor se puede actualizar posteriormente a medida que se ejecuta el programa. El valor asociado con el nombre puede cambiar (variar) en el tiempo.
 
