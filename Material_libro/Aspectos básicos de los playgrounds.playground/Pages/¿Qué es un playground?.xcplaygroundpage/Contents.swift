## ¿Qué es un playground?
Un playground es un lugar donde puedes jugar y, a la vez, experimentar con el código y ver resultados al instante sin ningún tipo de interrupciones. Un playground combina diferentes partes que explican conceptos (como este texto) con código interactivo que puedes modificar.

La _línea de código_ más simple que se puede escribir es un número:
 */
765
/*:
👆¿Puedes ver la diferencia en el número anterior?
Este tipo de texto es *código editable*, es decir, puedes escribirlo o modificarlo para lograr los resultados que desees.

En el caso de los números negativos, se debe usar el signo menos:
 */
