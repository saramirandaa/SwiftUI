¿Ves que los números aparecen en el área gris a la derecha? 👉

Esa área se denomina _barra lateral de resultados_. A medida que agregas o cambias código, el playground ejecuta tu código nuevamente y actualiza los resultados en la barra lateral.

 - callout(Experimento): 
   - Haz clic en una línea de código para mover el cursor hacia allí.
   - Escribe para comenzar a editar código.
   - Cambia los números algunas veces.
   - Agrega algunos números nuevos, cada uno en una línea.

 Observa que, cada vez que realizas un cambio, los resultados se actualizan en la barra lateral.

