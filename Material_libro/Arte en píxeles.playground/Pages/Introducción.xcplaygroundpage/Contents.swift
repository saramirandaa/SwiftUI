## Arte en píxeles

 Si nos remontamos a los viejos tiempos de la informática personal, cuando las pantallas con mapas de bits eran nuevas, los píxeles eran mucho más preciados de lo que son en la actualidad. Por ejemplo, en la década de 1980, la computadora Apple II tenía una pantalla de 40 x 40 píxeles, por lo que los gráficos tenían muy baja resolución. Además, los colores no eran óptimos tampoco. En ese momento, muchas pantallas tenían píxeles que se activaban o se desactivaban, y las pantallas más sofisticadas tenían solo unos pocos colores. La computadora Apple II les dio a los usuarios la posibilidad de elegir 16 colores en modo de gráficos de baja resolución.
 
 En aquella época, los programadores hacían muchos de sus trabajos píxel por píxel y creaban gráficos en la pantalla de forma artesanal.
 
 En este playground, crearás gráficos de baja resolución de a un píxel por vez. Es probable que no lo hagas como un desarrollador de apps, pero es una forma divertida de explorar algunos aspectos básicos de los gráficos y ponerte en el lugar de los desarrolladores que te precedieron.
 
 A continuación, comenzarás con los píxeles.

