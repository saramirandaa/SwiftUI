## Resumen

 Las enumeraciones (o “enum”) se usan cuando quieres representar un valor dentro de un grupo de valores relacionados. Cada valor posible se llama “case”.
 
 Al crear una enum, creas un tipo nuevo. Las instancias de ese tipo solo pueden tener valores que coincidan con uno de los elementos “case” especificados.
 
 Utilizar las enums puede facilitar la escritura y la lectura del código, porque siempre queda claro cuáles son los valores posibles y qué significan.
 
 Puedes comparar valores de enum con `==` o usar una instrucción “switch” para probar todos los valores posibles.
 
 Al igual que las instrucciones condicionales “if”, las instrucciones “switch” son otra forma de tomar decisiones en el código. Las instrucciones “switch” funcionan muy bien con las enums, pero se pueden usar para cambiar cualquier tipo de valor.
 
 Como las instrucciones “switch” deben ser exhaustivas, es necesario que incluyas todos los valores posibles. Para incluir todos los valores que no especificaste, puedes usar un elemento “case” `default`. 
 
 Al igual que con las structs, puedes agregar propiedades calculadas y métodos a las enums.
 
