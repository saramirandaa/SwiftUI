## Resumen

 Cada constante o variable que utilizas en Swift tiene un tipo.
 
 Para las variables, el valor puede cambiar, pero el tipo no.

 Los tipos describen lo siguiente:
- Qué representa un valor.
- Qué puede hacer un valor.
- Dónde puedes usar un valor.
 
 Cada línea de código que escribes se comprueba para evitar que utilices tipos de manera incorrecta.
 
 Swift puede inferir el tipo de una constante o de una variable por el valor que tienen asignado.
 
 Para especificar explícitamente el tipo de una constante o de una variable, puedes usar la anotación de tipo.
 
