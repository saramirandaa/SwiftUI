/*:
De la misma manera que cuando te vistes realizas varias actividades, cuando llamas a la función `print()`, suceden distintas cosas. Por ejemplo:
- Todo lo que le proporciones, incluidos los números, se convierte en una cadena.
- Se agrega un carácter de salto de línea, de modo que cada llamada a `print()` termina en una nueva línea.
- Así, la cadena se muestra en la consola.

En este caso, estás llamando a una función que ya creó otra persona. No es necesario conocer todos los detalles sobre cómo funciona `print()` para poder llamarla.

 La posibilidad de reutilizarlas es en gran parte lo que hace a las funciones tan potentes. Proporcionan una manera de combinar pasos detallados para generar una definición que se puede usar una y otra vez.

