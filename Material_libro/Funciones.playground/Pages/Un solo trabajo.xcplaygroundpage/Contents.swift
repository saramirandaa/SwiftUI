/*:
## Un solo trabajo

 Puedes combinar el código que podrías usar más de una vez en una función. Así como *declaraste* los nombres de las constantes con `let`, puedes declarar una función con `func`.
 
 Dado que las funciones pueden contener varias líneas de código, la declaración es un poco más complicada. Esta es una función muy simple:
 */
func rowTheBoat() {
    print("Rema, rema, rema tu bote”)
    print("Alegre por el río")
}
/*:
 El _nombre_ de la función es `rowTheBoat()` (remar el bote). Aprenderás más detalles sobre el uso de los paréntesis `()` más adelante, pero, por el momento, recuerda que cualquier identificador seguido por paréntesis es una función.

 El código que se encuentra entre dos llaves: `{`...`}` se conoce como *cuerpo* de la función.
 
 Estas líneas de código son diferentes a otras que has visto en los playgrounds. No se muestra nada en la barra lateral de resultados ni en la consola. Esto se debe a que, al _declarar_ una función, solo describes lo que haría la función en caso de ejecutarla. 
 
 Para ejecutar realmente el código, debes _llamar_ a la función. Para llamar a la función, debes escribir su nombre. Elimina el comentario de la siguiente línea:
 */
//rowTheBoat()
